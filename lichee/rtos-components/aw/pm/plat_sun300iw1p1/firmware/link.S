OUTPUT_ARCH("riscv")
OUTPUT_FORMAT("elf32-littleriscv","elf32-littleriscv","elf32-littleriscv")
ENTRY(standby_main)

MEMORY
{
	SRAM : ORIGIN = CONFIG_ARCH_PM_FIRMWARE_BASE, LENGTH = CONFIG_ARCH_PM_FIRMWARE_SIZE
}

REGION_ALIAS("REGION_PARAM", SRAM);
REGION_ALIAS("REGION_TEXT",  SRAM);
REGION_ALIAS("REGION_DATA",  SRAM);
REGION_ALIAS("REGION_BSS",   SRAM);
REGION_ALIAS("REGION_HEAP",   SRAM);

SECTIONS
{
	.param : {
		_sram_standby_head_start = .;
		*(.standby_head)
		_sram_standby_head_end = .;
	} > REGION_PARAM

	. = ALIGN(4);
	.text : {
		_sram_standby_code_start = .;
		*(.text)
		*(.text*)
		*(.rodata.*)
	} > REGION_TEXT
	.data : {
		*(.data)
		*(.data*)
		_sram_standby_code_end = .;
	} > REGION_DATA

	. = ALIGN(4);

	.bss : {
		_sram_standby_bss_start = .;
		*(.bss)
		*(.bss*)
		_sram_standby_bss_end = .;
	} > REGION_BSS

	. = ALIGN(4);
	_standby_stack_bottom = .;
	_standby_stack_top = _standby_stack_bottom + 0x400;
	. = _standby_stack_top;

	. = ALIGN(4);
	_dram_backup_start = .;
	_dram_backup_end  = CONFIG_ARCH_PM_FIRMWARE_BASE + CONFIG_ARCH_PM_FIRMWARE_SIZE;
}
