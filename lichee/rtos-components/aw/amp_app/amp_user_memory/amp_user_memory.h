#define USER_MEM_NAME(id) CONFIG_AMP_USER_MEMORY_DT_NODE_NAME_PREFIX#id

#define GET_USER_MEM_MEMBER_VAR(user_mem_id) \
	if (id == user_mem_id) \
	{ \
		return &resource_table.amp_user_memory##user_mem_id; \
	}

#define USER_MEM_MEMBER_DEFINITION(id) struct fw_rsc_carveout amp_user_memory##id;
#define USER_MEM_MEMBER_OFFSET(id) offsetof(struct sunxi_resource_table, amp_user_memory##id),

#define USER_MEM_MEMBER_DATA(id) \
	.amp_user_memory##id = { \
		.type = RSC_CARVEOUT, \
		.da = -1, \
		.pa = -1, \
		.len = 0, \
		.flags = MEM_NONCACHEABLE, \
		.reserved = 0, \
		.name = USER_MEM_NAME(id), \
	},


