#define AMP_USER_MEMORY_INTERNAL_API_DEFINITION \
	const struct fw_rsc_carveout *resource_table_get_amp_user_memory(uint32_t id) \
	{ \
		if (id >= AMP_USER_MEMORY_MAX_NUM) \
			return NULL; \
		GET_USER_MEM_0_MEMBER_VAR \
		GET_USER_MEM_1_MEMBER_VAR \
		GET_USER_MEM_2_MEMBER_VAR \
		GET_USER_MEM_3_MEMBER_VAR \
		return NULL; \
	}

