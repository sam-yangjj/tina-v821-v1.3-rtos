#define SUNXI_CCMU_AON_BASE		0x4A010000

#define CCMU_BUS_CLK_GATING1_REG		(SUNXI_CCMU_APP_BASE + 0x0084)
#define CCMU_BUS_RESET1_REG             (SUNXI_CCMU_APP_BASE + 0x0094)
#define CCMU_SDMMC0_CLK_REG             (SUNXI_CCMU_APP_BASE + 0x0014)
#define CCMU_SDMMC1_CLK_REG             (SUNXI_CCMU_APP_BASE + 0x005C)
