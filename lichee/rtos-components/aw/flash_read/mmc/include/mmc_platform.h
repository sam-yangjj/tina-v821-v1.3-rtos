#define GPIO_POW_MODE_REG (0x0340)
#define SMHC0_BGR_REG_SMHC0_RST_OFFSET 20
#define SMHC0_BGR_REG_SMHC0_GATING_OFFSET 20

#define HOSC_CC_ID						AW_AON_CCU
#define HOSC_CLK_ID						CLK_AON_HOSC_FAKE_MUX

#include <hal_time.h>
#include <hal_clk.h>
#include <hal_cache.h>

#define sleep  hal_sleep
