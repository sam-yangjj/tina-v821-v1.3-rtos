static int gc05a2_sensor_vts;
static int sensor_s_exp(int id, unsigned int exp_val)
{
	unsigned char explow, exphigh;
	unsigned int all_exp, frame_length = gc05a2_sensor_vts;

	sensor_dbg("sensor_set_exp = %d, Done!\n", exp_val);
	all_exp = exp_val / 16;
	if (all_exp > 0x7fef)
		all_exp = 0x7fef;
	else if (all_exp < 1)
		all_exp = 1;

	frame_length = (all_exp + 16) > gc05a2_sensor_vts ? (all_exp + 16) : gc05a2_sensor_vts;

	if (frame_length > 0x7fff)
		frame_length = 0x7fff;

	exphigh  = (unsigned char) ((frame_length >> 8) & 0xff);
	explow  = (unsigned char) (frame_length & 0xff);
	sensor_write(id, 0x0340, exphigh);
	sensor_write(id, 0x0341, explow);

	sensor_write(id, 0x0202, (all_exp >> 8) & 0xff);
	sensor_write(id, 0x0203, all_exp & 0xff);

	sensor_dbg("sensor_s_exp %d\n", all_exp);

	return 0;
}

static int sensor_s_gain(int id,int gain_val)
{
	unsigned int All_gain;

	if (gain_val < 16)
		gain_val = 16;
	if (gain_val > 16 * 64)
		gain_val = 16 * 64;

	if (gain_val == 16 * 64)
		All_gain = 0xffff;
	else
		All_gain = gain_val << 6;

	sensor_write(id, 0x0204, (All_gain >> 8) & 0xff);
	sensor_write(id, 0x0205, All_gain & 0xff);
	sensor_dbg("sensor_s_gain %d\n", All_gain);

	return 0;
}

static int sensor_s_exp_gain(int id, struct sensor_exp_gain *exp_gain)
{
	int exp_val, gain_val;
	// int shutter = 0, frame_length = 0;

	exp_val = exp_gain->exp_val;
	gain_val = exp_gain->gain_val;

	if (gain_val < 1 * 16)
		gain_val = 16;

	glb_exp_gain.exp_val = exp_val;
	glb_exp_gain.gain_val = gain_val;

	sensor_s_exp(id, exp_val);
	sensor_s_gain(id, gain_val);
	sensor_print("gain_val:%d, exp_val:%d\n", gain_val, exp_val);

	return 0;
}


