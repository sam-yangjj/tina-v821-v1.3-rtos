static int sensor_reg_init(int id, int isp_id)
{
	int ret = 0;
	__attribute__((__unused__)) int ispid = clamp(isp_id, 0, ISP_GET_CFG_NUM - 1);
	struct sensor_exp_gain exp_gain;

	ret = sensor_write_array(id, sensor_default_regs,
				 ARRAY_SIZE(sensor_default_regs));
	if (ret < 0) {
		sensor_err("write sensor_default_regs error\n");
		return ret;
	}

	if (current_win[id]->regs)
		ret = sensor_write_array(id, current_win[id]->regs, current_win[id]->regs_size);
	if (ret < 0)
		return ret;

	gc05a2_sensor_vts = current_win[id]->vts;
#if defined CONFIG_ISP_READ_THRESHOLD || defined CONFIG_ISP_ONLY_HARD_LIGHTADC
	if (ispid == 0) {
		exp_gain.exp_val = clamp(*((unsigned int *)ISP0_NORFLASH_SAVE + 2), 16, gc05a2_sensor_vts << 4);
		exp_gain.gain_val = clamp(*((unsigned int *)ISP0_NORFLASH_SAVE + 1), 16, 64 << 4);
	} else {
		exp_gain.exp_val = clamp(*((unsigned int *)ISP1_NORFLASH_SAVE + 2), 16, gc05a2_sensor_vts << 4);
		exp_gain.gain_val = clamp(*((unsigned int *)ISP1_NORFLASH_SAVE + 1), 16, 64 << 4);
	}
	sensor_s_exp_gain(id, &exp_gain);
#else
	exp_gain.exp_val = 16; /* 60fps ae table max_idx/2 corresponding to exp&gain */
	exp_gain.gain_val = 10448;
	sensor_s_exp_gain(id, &exp_gain);
	/* sensor stream on */
	sensor_write(id, 0x0110, 0x01);
	sensor_write(id, 0x0100, 0x01); /* stream_on */
#endif

	sensor_dbg("%s\n", __func__);

	return 0;
}

