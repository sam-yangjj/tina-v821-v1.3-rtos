static int sensor_detect(int id)
{
	data_type rdval;

	sensor_read(id, 0x0000, &rdval);
	if ((rdval & 0x0f) != 0x02)
		return -ENODEV;

	sensor_read(id, 0x0001, &rdval);
	if (rdval != 0x19)
		return -ENODEV;
	sensor_print("find the sony IMX219 ***********\n");

	return 0;
}

static int sensor_init(int id)
{
	int ret;

	sensor_dbg("sensor_init\n");

	/*Make sure it is a target sensor */
	ret = sensor_detect(id);
	if (ret) {
		sensor_err("chip found is not an target chip.\n");
		return ret;
	}

	return 0;
}

