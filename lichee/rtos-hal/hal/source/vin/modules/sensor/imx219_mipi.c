#if CONFIG_SENSOR_CHECK
static int sensor_check_func(int id)
{
	data_type rdval = 0;
	data_type rdval_otp = 0;
	int i, addr = 0x3204;

	sensor_read(id, 0x0000, &rdval);
	if ((rdval & 0x0f) != 0x02)
		return -ENODEV;

	sensor_read(id, 0x0001, &rdval);
	if (rdval != 0x19)
		return -ENODEV;

	sensor_write(id, 0x0100, 0x00);
	sensor_write(id, 0x3302, 0x05);
	sensor_write(id, 0x3303, 0xA0);
	sensor_write(id, 0x012A, 0x18);
	sensor_write(id, 0x012B, 0x00);
	sensor_write(id, 0x3300, 0x00);
	sensor_write(id, 0x3200, 0x01);
	sensor_write(id, 0x3202, 0x05);

	for (i = 0; i < 8; i++) {
		sensor_read(id, addr, &rdval_otp);
		sensor_print("addr = 0x%x  data = 0x%x \n", addr, rdval_otp);
		hal_usleep(10 * 1000);
		if (rdval_otp == 0xd8) {
			sensor_print("find the sony IMX219 ***********\n");
			check_done = true;
			return 0;
		}
		addr = addr + 1;
	}
	check_done = true;

	return -1;
}
#endif

static int sensor_reg_init(int id, int isp_id)
{
	int ret = 0;
	__attribute__((__unused__)) int ispid = clamp(isp_id, 0, ISP_GET_CFG_NUM - 1);
	struct sensor_exp_gain exp_gain;

	ret = sensor_write_array(id, sensor_default_regs,
				 ARRAY_SIZE(sensor_default_regs));
	if (ret < 0) {
		sensor_err("write sensor_default_regs error\n");
		return ret;
	}

	if (current_win[id]->regs)
		ret = sensor_write_array(id, current_win[id]->regs, current_win[id]->regs_size);
	if (ret < 0)
		return ret;

	imx219_sensor_vts = current_win[id]->vts;
#if defined CONFIG_ISP_READ_THRESHOLD || defined CONFIG_ISP_ONLY_HARD_LIGHTADC
	if (ispid == 0) {
		exp_gain.exp_val = clamp(*((unsigned int *)ISP0_NORFLASH_SAVE + 2), 16, imx219_sensor_vts << 4);
		exp_gain.gain_val = clamp(*((unsigned int *)ISP0_NORFLASH_SAVE + 1), 16, 64 << 4);
	} else {
		exp_gain.exp_val = clamp(*((unsigned int *)ISP1_NORFLASH_SAVE + 2), 16, imx219_sensor_vts << 4);
		exp_gain.gain_val = clamp(*((unsigned int *)ISP1_NORFLASH_SAVE + 1), 16, 64 << 4);
	}
	sensor_s_exp_gain(id, &exp_gain);
#else
	 exp_gain.exp_val = 10448; /* 60fps ae table max_idx/2 corresponding to exp&gain */
	 exp_gain.gain_val = 16;
	 sensor_s_exp_gain(id, &exp_gain);
	 /* sensor stream on */
	 //sensor_write(id, 0x0110, 0x01);
	 sensor_write(id, 0x0100, 0x01); /* stream_on */
#endif

#if CONFIG_SENSOR_CHECK
#if defined CONFIG_ISP_READ_THRESHOLD || defined CONFIG_ISP_ONLY_HARD_LIGHTADC

	if (!check_done) {
		/* only do once */
		ret = sensor_check_func(id);
		if (ret < 0) {
			sensor_err("imx219 check fail!!!\n");
			return -1;
		} else {
			sensor_write(id, 0x0100, 0x01);
		}
	}
#endif
#endif

	sensor_print("%s\n", __func__);

	return 0;
}

static int sensor_s_stream(int id, int isp_id, int enable)
{
	if (enable && sensor_stream_count[id]++ > 0)
		return 0;
	else if (!enable && (sensor_stream_count[id] == 0 || --sensor_stream_count[id] > 0))
		return 0;


	sensor_dbg("%s on = %d\n", __func__, enable);

	if (!enable)
		return 0;

	return sensor_reg_init(id, isp_id);
}

static int sensor_s_switch(int id)
{
#if defined CONFIG_ISP_FAST_CONVERGENCE || defined CONFIG_ISP_HARD_LIGHTADC
	struct sensor_exp_gain exp_gain;
	int ret = -1;
	sensor_write(id, 0x0100, 0x00);

	imx219_sensor_vts = current_switch_win[id]->vts;

	if (glb_exp_gain.exp_val && glb_exp_gain.gain_val) {
		exp_gain.exp_val = glb_exp_gain.exp_val;
		exp_gain.gain_val = glb_exp_gain.gain_val;
	} else {
		exp_gain.exp_val = 24048;
		exp_gain.gain_val = 16;
	}

	if (current_switch_win[id]->switch_regs)
		ret = sensor_write_array(id, current_switch_win[id]->switch_regs, current_switch_win[id]->switch_regs_size);
	else
		sensor_err("cannot find 600p80fps to %dp%dfps reg\n", current_switch_win[id]->height, current_switch_win[id]->fps_fixed);
	if (ret < 0)
		return ret;
	sensor_s_exp_gain(id, &exp_gain); /* make switch_regs firstframe  */
	//sensor_write(id, 0x0110, 0x01);
#if CONFIG_SENSOR_CHECK
	if (!check_done) {
		/* only do once */
		ret = sensor_check_func(id);
		if (ret < 0) {
			sensor_err("imx219 check fail!!!\n");
			return -1;
		} else {
			sensor_write(id, 0x0100, 0x01);
		}
	}
#else
	sensor_write(id, 0x0100, 0x01); /* stream_on */
#endif

	sensor_print("600p80fps to %dp%dfps success\n", current_switch_win[id]->height, current_switch_win[id]->fps_fixed);
#endif
	return 0;
}

